#include<stdio.h>
int main(void)
{
    int i, k, m;
    i = 10;
    k = 20;
    m = (1 > 2) && (k = 5);
    printf("m = %d, k = %d\n", m, k);
    return 0;
}
/*
在CodeBlocks中运行的结果是：
------------------------
m = 0, k = 20
------------------------
总结：&&左边的表达式为假  右边的表达式肯定不执行
      ||左边的表达式为真  右边的表达式肯定不会执行
*/
